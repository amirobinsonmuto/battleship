function shuffleArray(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    let j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

export default function returnRandomNum() {
  let arr = [...Array(90).keys()];
  return shuffleArray(arr).pop();
}
